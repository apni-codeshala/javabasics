public class MathClass {
    public static void main(String[] args) {
        System.out.println("E = " + Math.E);
        System.out.println("PI = " + Math.PI);
        System.out.println("MAX = " + Math.max(3, 4));
        System.out.println("MIN = " + Math.min(3, 6));
        System.out.println("Absolute = " + Math.abs(45.4664646));
        System.out.println("2 POW 3= " + Math.pow(2, 3));
        System.out.println("CEIL = " + Math.ceil(10.7));
        System.out.println("FLOOR = " + Math.floor(10.5));
        System.out.println("cos(a) = " + Math.cos(10.7));
        System.out.println("cosh(a) = " + Math.cosh(10.3));
        System.out.println("sin(a) = " + Math.sin(10.7));
        System.out.println("sinh(a) = " + Math.sinh(10.7));
        System.out.println("log10(a) = " + Math.log10(10.7));
        System.out.println("log(a) = " + Math.log(10.7));
        System.out.println("sqrt(a) = " + Math.sqrt(9));
    }
}
